char	*ft_strlowcase(char *str)
{
	int	x;

	x = 0;
	while (str[x])
	{
		if (str[x] <= 'Z' && str[x] >= 'A')
		{
				str[x] = str[x] +32;
		}
		x++;
	}
	return (str);
}
