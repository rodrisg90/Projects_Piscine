char	*ft_strupcase(char *str)
{
	int	x;

	x = 0;
	while (str[x])
	{
		if (str[x] <= 'z' && str[x] >= 'a')
		{
				str[x] = str[x] -32;
		}
		x++;
	}
	return (str);
}
